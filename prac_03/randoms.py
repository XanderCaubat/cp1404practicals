import random

print((random.randint(5, 20)))  # The smallest number seen was 5 and the largest number seen was 18.
print(random.randrange(3, 10, 2))  # The smallest number seen was 3 and largest was 9. No, line 2 could not produce 4.
print(random.uniform(2.5, 5.5))     # The smallest number seen was 2.5436002992019238 and largest was 5.158785122603284.
